package org.example.homework3;

public class MinAndMax {
    public static void main(String[] args) {
        int num = 11;
        int num1 = 5;

        int max = Math.max(num, num1);
        System.out.println("Maximum: " +  max);

        int min = Math.min(num, num1);
        System.out.println("Minimum: " + min);

    }
}
